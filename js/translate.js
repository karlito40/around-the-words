function _ts(key, o, noAccent)
{
	if(typeof TRANS[key] == "undefined") {
		return key;
	}

	var s = TRANS[key];

	if(o) s = Util.String2.strtr(s, o);
	if (noAccent) s = Util.String2.noAccent(s);

	return s;

};
